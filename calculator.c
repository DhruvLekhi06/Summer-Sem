#include <stdio.h>

int main() {
    int a = 5, b = 3;
    int sum = a + b;
    int diff = a - b;
    float div = (float)a / b;
    int prod = a * b;
    printf("Addition: %d\n", sum);
    printf("Subtraction: %d\n", diff);
    printf("Division: %.2f\n", div);
    printf("Multiplication: %d\n", prod);
    return 0;
}
